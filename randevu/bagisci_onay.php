				<b><font color="#c10100">•</font></b>  Kan bağıiçıları tercihen kan bağışından 2 saat öncesine kadar tam bir öğün yemiş olmalıdır.<br>
				<b><font color="#c10100">•</font></b>  Kan bağışçısı bağış öncesi alkol kullanmamış olmalı ve alkol etkisinde olmamalıdır.<br>
				<b><font color="#c10100">•</font></b>  Normal yağsız besinler alınmış olmalı ve mümkün olduğunca fazala sıvı tüketilmiş olmalıdır.<br>
				<b><font color="#c10100">•</font></b>  Bağıştan önceki yarım saat içinde kafeyin içeren içecekler(kahve,kola,kahveli içecekler vb.) içilmesi tavsiye edilir.
