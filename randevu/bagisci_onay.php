				Kan bağışçısı sorgulama formunda yanlış beyanda bulunmanız kendi sağlınızada zarar vermenize yol açabilir.
				<br><br>
				İhtiyacı olan hastalara ihtiyacı olan kan/kan bileşenlerinin temin edilmesi ve hazırlanması gibi aşamalarda kan bağışı merkezi çalışanları olarak bizler kan bağışlayacısı olarak sizle hastalara karşı çok büyük sorumluluklar taşımaktayız.
				<br><br>
				
				(Kan ve kan ürünleri kanunu kapsamında bilinçli olarak yanlış bilgi verilmesi suçtur.56/24 sayılı yasa, madde 6/10'da <b><font color="#262525">"Kan yolu ile bulaşan bir hastalığı veya böyle bir hastalık taşıma riski olduğunu bilip bu durumu saklayarak kan verenlere bir yıldan üç yıla kadar hapis ve 500 gün adli para cezası verilir."</font></b> ibaresi yer almaktadır.
