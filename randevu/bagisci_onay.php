			<br><br>
				
				Kan bağışçısı için aşağıda sıralanan koşullar yerine getirilmelidir;
				<br><br>
				Yasal mevzuat gereğince bağışçılar;İsim-soyisim,doğum tarihi, tc kimlik numarasını içeren resimli ve geçerli bir kimliğe ve kalıcı adres bilgilerini vererek kendilerini tanıtmalıdır.
				<br><br>
				<b><font color="#c10100">•</font></b>  18 ile 65 yaş arasında olanlar kan bağışlayabilir, daha önce kan bağışı yapmamış olanlar 60 yaşına kadar kan bağışı yapabilirler.<br>
				<b><font color="#c10100">•</font></b>  Nabız düzenli ve dakikada 50 ile 100 arasında olmalıdır.<br>
				<b><font color="#c10100">•</font></b>  Kan basıncı bir çok etkene bağlı olarak değişmekle birlikte esasen sistolik basınç(Büyük Tansiyon) en az 90 mmHg, en çok 180 mmHg ve diyastolik basınç(küçük tansiyon) en az 60 mmHg en çok 100 mmHg olmalıdır.<br>
				<b><font color="#c10100">•</font></b>  Hemoglobin değerleri kadınlarda en az 12,5 g/dL en çok 16,5 g/dL, erkeklerde en az 13,5 g/dL en çok 18 g/dL olmalıdır.<br>
				<b><font color="#c10100">•</font></b> Kadınlar son bir yıl içerisinde en fazla 3 erkekler ise en fazla 4 kez tam kan bağışında bulunabilir.<br>
				<b><font color="#c10100">•</font></b>  Kan bağışçının vücut ağırlığı en az 50 kg olmalıdır.<br>
				<b><font color="#c10100">•</font></b>  Kan bağışçının vücut sıcaklığı 37,5 derecenin üstünde olmamalıdır.
