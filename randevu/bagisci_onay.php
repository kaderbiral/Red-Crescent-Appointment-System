			<b><font color="#262525">Kan Bağışı Merkezine Hoş Geldiniz</font></b>
			<br><br>
			<b><font color="#262525">KAN BAĞIŞLAMAK İSTEDİĞİNİZ İÇİN TEŞEKKÜR EDERİZ. "KAN BAĞIŞÇISI KAYIT VE SORGULAMA FORMLARINI DOLDURMADAN ÖNCE AŞAĞIDAKİ AÇIKLAMALARI LÜTFEN DİKKATLİCE OKUYUNUZ!"</font></b>
			<br><br>
				
			<b><font color="#262525">"Kan bağışçısı sorgulama formundaki sorular sizi ve sizin kanınızı alacak olan hastaları korumak amacıyla düzenlenmiştir."</font></b>
			<br><br> 
				
				Kan bağışına uygun olduğunuzun belirlenmesi için geçirdiğiniz hastalıklar, aldığınız ilaçlar ve cinsel yaşamınızla ilgili sorular sorulacaktır. Aids ve sarılık gibi hem kan hem de cinsel yolla hastalıklar için enfeksiyon bulaşanın gerçekleştiği andan testlerde pozitif hale gelinceye kadar geçen ve tanı konunmayan bi dönem vardır.Bu döneme pencere dönemi denilmektedir. Pencere dönemindeki bir kişinin test sonuçları temiz görünmekle birlikte kan bağışında bulunduğu zaman hastalara enfeksiyon bulaşabilmektedir. Bu nedenle formu doldururken vereceğiniz yanıtların doğru ve samimi olması sizin ve kanınızı alacak olan hastaların uğrayacağı zararın önlenmesi açısından büyük önem taşımaktadır.
				Unutmayınız ki ; bağışlayacağınız kan korumasız, şuuru kapalı, kanı reddetme şansı olmayan bir ikişiye veya yeni doğmuş bir bebeğe verilebilir.Vereceğiniz tüm yanıtlar kişisel bilgileriniz, sağlık durumunuz ve test sonuçlarınız 56/24 sayılı kan ve kan ürünleri yasası ve ilgili yönetmelik gereğince gizli tutulacaktır.<br><br>
				
			<b><u><font color="#262525">Aşağıdaki Durumların Biri Sizin İçin Geçerliyse Lütfen Kan Vermekten Vazgeçiniz!</font></u></b>
			<br><br> 
				
				<b><font color="#c10100">1)</font></b> Sebebi açıklanmayan ateş, kilo kaybı, gece terlemesi, büyümüş lenf bezi veya kitlesi, deride mor lekeler, ağız ve boğazda beyaz döküntüler, uzun süren ve iyileşmeyen öksürük veya ishaliniz varsa,<br>
				<b><font color="#c10100">2)</font></b> Son bir yıl içinde kondom kullnarak ya da kullanmadan para karşılığı cinsel ilişkide bulunmuşsanız,<br>
				<b><font color="#c10100">3)</font></b> Para kazanmak için cinsel ilişkide bulunmuşsanız,<br>
				<b><font color="#c10100">4)</font></b> Kondom kullanrak ya da kullanmadan ve bir defalığına bile olsa oral veya anal yolla erkek erkeğe cinsel ilişkide bulunmuşsanız,<br>
