			<b><u><font color="#262525">Herhangi Bir Hastalık Şüphesi Duyuyor Ve Sadece Test Yaptırmak İçin Kan Bağışlıyorsanız Kan Bağışından Vazgeçiniz Ve Bir Hastanenin Enfeksiyon Hastalıkları Bölümüne Başvurunuz!</font></u></b>
