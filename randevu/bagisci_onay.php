				Kan alma işlemi tamamlandıktan sonra iğne koldan çıkarılır hemen ardından iğnenin çıkarıldığı bölgeye uygun ve temiz pansuman malzemesi ile baskı uygulanır.Kan bağışçısı bağıştan sonra yeterli süre kan alma yatağında bekletilir.<br><br>
				
