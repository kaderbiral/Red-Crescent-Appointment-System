				Kan bağışı sırasında ve sonrasında önemli bir sorunla karşılanması beklenmemektedir.Nadiren baş dönmesi, terleme, çarpıntı, bulantı, kusma, bayılma, kas spazmları, iğne giriş yerinde şişme ve morarma gibi sorunlar ile karşılaşılabilmektedir.Kalp yada sinir sistemi kaynaklı hayati tehlike arz eden sorunlar ise çok nadir olarak görülür. Ayrıca düzenlii kan bağışında kan demir deposu düzeyi düşebilmektedir.Kan bağışı merkezi personeli bu tür durumlarda gerekli müdahaleyi yapabilecek bilgi ve deneyime sahiptir.Kan bağışçısı bağış esnasında ve sonrasında yetkili personelin tıbbi tavsiye ve yöneldirmesine uymalıdır.
